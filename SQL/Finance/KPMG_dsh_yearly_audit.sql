SELECT PAY.pt_id,
	PAY.unit_seq_no,
	CAST(PLM.Adm_Date AS DATE) AS [Adm_Date],
	CAST(PLM.Dsch_Date AS DATE) AS [Dsch_Date],
	CAST(PAY.pay_date AS DATE) AS [Svc_Date],
	[Source] = CASE
		WHEN PAY.pyr_cd = PLM.Pyr1_Co_Plan_Cd
			THEN 'Primary'
		ELSE 'Secondary'
		END,
	PAY.pyr_cd,
	PDV.pyr_cd_desc,
	SUM(PAY.tot_pay_adj_amt) AS [Trans_Totals]
FROM smsmir.pay AS PAY
INNER JOIN SMSDSS.BMH_PLM_PtAcct_V AS PLM ON PAY.pt_id = PLM.PT_NO	
	AND PAY.unit_seq_no = PLM.unit_seq_no
INNER JOIN SMSDSS.pyr_dim_v AS PDV ON PAY.pyr_cd = PDV.src_pyr_cd
	AND PAY.orgz_cd = PDV.orgz_cd
WHERE (
	PAY.pay_cd BETWEEN '09600000' AND '09699999'
	OR PAY.pay_cd BETWEEN '00990000' AND '00999999'
	OR PAY.pay_cd BETWEEN '09900000' AND '09999999'
	OR PAY.pay_cd IN (
		'00980300','00980409','00980508','00980607','00980656','00980706','00980755','00980805','00980813','00980821','09800095',
		'09800277','09800301','09800400','09800459','09800509','09800558','09800608','09800707','09800715','09800806','09800814','09800905',
		'09800913','09800921','09800939','09800947','09800962','09800970','09800988','09800996','09801002','09801010','09801028','09801036','09801044',
		'09801051','09801069','09801077','09801085','09801093','09801101','09801119', '09801127','00990143','09801135','00990150','09801143','09801150','00990168',
		'00990176','09801168','00990184','09801176'
	)
)
AND DATEPART(YEAR, PLM.Dsch_Date) = 2019
AND (PAY.unit_seq_no = '0' OR SUBSTRING(CAST(PAY.UNIT_SEQ_NO AS VARCHAR), 1, 2) = '19')
AND plm.PtNo_Num IN (

)
GROUP BY PAY.pt_id,
	PAY.unit_seq_no,
	CAST(PLM.Adm_Date AS DATE),
	CAST(PLM.Dsch_Date AS DATE),
	CAST(PAY.pay_date AS DATE),
	PLM.Pyr1_Co_Plan_Cd,
	PAY.pyr_cd,
	PDV.pyr_cd_desc
HAVING SUM(PAY.tot_pay_adj_amt) != 0